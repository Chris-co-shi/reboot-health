package com.indigobyte.reboothealth.profile.domain;

/**
 * 身体部位枚举，用于标识康复计划或健康档案中涉及的具体身体区域。
 */
public enum BodyRegion {
    /**
     * 心血管系统
     */
    CARDIOVASCULAR,

    /**
     * 颈椎
     */
    CERVICAL_SPINE,

    /**
     * 肩颈区域
     */
    SHOULDER_NECK,

    /**
     * 下背部（腰椎区域）
     */
    LOWER_BACK,

    /**
     * 髋部
     */
    HIP,

    /**
     * 足底
     */
    FOOT_SOLE,

    /**
     * 跟腱
     */
    ACHILLES_TENDON,

    /**
     * 全身
     */
    FULL_BODY,

    /**
     * 其他部位
     */
    OTHER
}
